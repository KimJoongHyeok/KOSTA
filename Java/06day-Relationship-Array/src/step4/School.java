package step4;

public class School {
	private String name;
	private Student Student;
	
	public School(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Student getStudent() {
		return Student;
	}
	public void setStudent(Student Student) {
		this.Student = Student;
	}
	
	
}
